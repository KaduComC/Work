class User {
  final String id;
  final String name;
  final String nickName;
  final String cpf;
  final String email;
  final String phoneNumber;
  final String password;
  final String avatar;

  const User({
    required this.id,
    required this.name,
    required this.nickName,
    required this.cpf,
    required this.email,
    required this.phoneNumber,
    required this.password,
    required this.avatar,
  });
}
